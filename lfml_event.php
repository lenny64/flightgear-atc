<h2>What to say to ATC, and why ATC won't kill you</h2>
<p>
    The main purpose of an <abbr title="Air Traffic Controller">ATC</abbr> is to help you and guide you to fly from a <b>point A</b> to a <b>point B</b>.
    <br/>
    To do that, some rules are important for your own security. It is important to follow them. I will give you a short tutorial of how to basically communicate with the ATC.
    <br/>
    <b>Remember that the more you communicate, the better is.</b>
</p>
<br/>
<h3>Example : lenny64 wants to fly visual from and to LFML</h3>
<h3>1. On the ground</h3>
<p>
    Before doing anything, you will have to tell to the ATC what you want. ATC will then give you some information and clearance (or not) to achieve your intentions.
    <br/>
    First of all, you will make sure that ATC does hear you (<b>radio check</b>). After that, you will tell you <b>what you want</b>. Simply.
</p>
<ul>
    <li>Radio check
        <br/><img src="./img/01_Stopped.png" alt="Stopped"/>
        <br/>You : <b>"Marseille tower, lenny64 with you, good day"</b>
        <br/>ATC : <b>"lenny64, hear you loud and clear, pass your message"</b> --> This sentence means that ATC hear you, and asks you to say what you want.</li>
    <br/>
    <li>Intentions
        <br/>You : <b>"I want to make a visual flight, south, lenny64"</b> --> To identify yourself, finish your message with your callsign
        <br/>ATC : <b>"lenny64, cleared for visual south at 2500 ft. Engine startup approved, report when ready to taxi."</b>  --> ATC will give you clearance for this visual request, and an altitude (to avoid collision with other aircrafts in the area). He will ask you to start your engines and call back when you are ready to taxi to the runway.
        <br/>You : <b>"Cleared for visual south 2500 ft, will call back when ready to taxi, lenny64"</b> --> As a good pilot, you copy the clearance (which means you heard and understood everything)
    </li>
    <br/>
    <li>Taxiing
        <br/>You : <b>"Marseille tower, lenny64 ready to taxi"</b>
        <br/>ATC : <b>"lenny64, taxi to holdng point 31R via Golf, Charlie, Delta7 on QNH 29.98"</b> --> This means you can taxi to the holding point (<b>without stepping onto the runway : wait just before</b>) via taxiways G C D7. ATC will also give you the altimeter (or QNH)
        <br/>You : <b>"Holding point 31R via Golf, Charlie, Delta7, QNH 29.98, lenny64"</b> --> As a good pilot, you copy the instruction
        <br/><img src="./img/02_Taxiing.png" alt="Taxiing"/>
    </li>
    <br/>
    <li>Holding point
        <br/><img src="./img/03_Holding-Point.png" alt="Holding"/>
        <br/>You : <b>"Holding short 31R, lenny64"</b> --> You wait before the runway, waiting for clearance to step onto the runway
        <br/>ATC : <b>"lenny64, line up on runway, report when ready for departure"</b>
        <br/>You : <b>"Lining up, will call back when ready, lenny64"</b> --> You line up and call back when you are ready
    </li>
    <br/>
    <li>Take-off
        <br/>You : <b>"Ready to take-off, lenny64"</b>
        <br/>ATC : <b>"lenny64, winds 310 at 10 knots, 31R clear to take-off"</b>
        <br/>You : <b>"Cleared to take-off, lenny64"</b> --> You copy the clearance. It's up to you to copy the weather info
        <br/><img src="./img/04_Take-off.png" alt="Take-off"/>
    </li>
    <br/>
</ul>

<h3>2. In the air</h3>

<ul>
    <li>On approach to Marseille
        <br/><img src="./img/05_Inbound.png" alt="Inbound"/>
        <br/>You : <b>"Marseille tower, lenny64 inbound visual, 2500 ft south west"</b> --> On visual approach, you tell who you are, where, and the altitude
        <br/>ATC : <b>"lenny64 descend 1500 ft, 31R, report base"</b> --> On visual, you have to vector yourself (unless you ask ATC to vector you) until you see the airport.
        <br/>You : <b>"1500 , will report base"</b> --> You copy the instructions
    </li>
    <br/>
    <li>Turning base
        <br/><img src="./img/06_Base-leg.png" alt="Base leg"/>
        <br/>You : <b>"Marseille tower, 31R on base, lenny64"</b>
        <br/>ATC : <b>"lenny64, winds 310 at 10 knots, 31R cleared to land"</b> --> If the traffic is not so busy and if you are at a nice altitude, ATC can give you the clearance to land.
        <br/>You : <b>"31R cleared to land, lenny64"</b>
    </li>
    <br/>
    <li>Turning final
        <br/><img src="./img/07_Final.png" alt="Final"/>
    </li>
</ul>

<h3>3. Landing</h3>

<ul>
    <li>Landing
        <br/><img src="./img/08_Touchdown.png" alt="Touchdown"/>
        <br/>You : <b>"Touchdown, lenny64"</b> --> This will inform ATC that you have successfully landed
        <br/>ATC : <b>"lenny64, vacate runway on your right, report vacated"</b>
    </li>
    <br/>
    <li>Taxi back to the terminal
        <br/><img src="./img/09_Taxi-to-terminal.png" alt="Taxiing to terminal"/>
        <br/>You : <b>"31R vacated, lenny64"</b>
        <br/>ATC : <b>"lenny64 say intentions"</b> --> ATC will ask you what you want to do on the ground (park, go for another take-off, ...)
        <br/>You : <b>"Requesting to taxi to terminal, lenny64"</b>
        <br/>ATC : <b>"Cleared to taxi to the terminal, report when ready to close flightplan"</b> --> Once you will be parked, you will be able to ask ATC to close your flightplan.
    </li>
    <br/>
    <li>Parked
        <br/><img src="./img/10_Parked.png" alt="Parked"/>
        <br/>You : <b>"Parked, ready to shutdown and close flightplan"</b>
        <br/>ATC : <b>"Roger, lenny64. I'm closing your flightplan. Good day"</b>
        <br/>You : <b>"Good day"</b>
    </li>
</ul>



